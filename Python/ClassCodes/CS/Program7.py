
day = input("Enter day : ")

if day == "Mon" or day == "Monday" :

	print("Start Of the Week")

elif day == "Fri" or day == "Friday" :

	print("Weekend is coming")

elif day == "Sat" or day == "Saturday" :

	print("Weekend Start")

elif day == "Sun" or day == "Sunday" :

	print("Relax, it's Sunday")

else :

	print("Yet Another Day")
