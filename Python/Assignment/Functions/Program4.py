
def operations(x,y):
	print("Addition : ", x + y)
	print("Subtraction : ", x - y)
	print("Multiplication : ", x * y)
	print("Division : ", x / y)
	print("Modulus : ", x % y)
	print("Floor Division : ", x // y)

num1 = int(input("Enter First Number : "))
num2 = int(input("Enter Second Number : "))

operations(num1, num2)
